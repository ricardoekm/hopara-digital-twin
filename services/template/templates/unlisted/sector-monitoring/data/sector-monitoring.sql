-- sector_monitoring_initialize_db

CREATE TABLE sm_sectors (
	id int4 NULL,
	sector_name varchar(255) NULL,
	PRIMARY KEY(id)
);

CREATE TABLE sm_sectors_sample_pos (
	id text NULL,
	point_2d geometry NULL,
	point_3d geometry NULL,
	line geometry NULL,
	rectangle geometry NULL,
	polygon geometry NULL,
	floor text NULL,
	hopara_scope text NULL,
	hopara_size integer NULL,
    hopara_size_reference_zoom numeric(24,8) NULL,
    hopara_color text NULL,
    hopara_scale integer NULL
);

CREATE INDEX sm_sectors_sample_pos_id ON sm_sectors_sample_pos USING btree (id);
CREATE INDEX sm_sectors_sample_pos_line_spatial ON sm_sectors_sample_pos USING gist (line);
CREATE INDEX sm_sectors_sample_pos_point_2d_spatial ON sm_sectors_sample_pos USING gist (point_2d);
CREATE INDEX sm_sectors_sample_pos_point_3d_spatial ON sm_sectors_sample_pos USING gist (point_3d);
CREATE INDEX sm_sectors_sample_pos_polygon_spatial ON sm_sectors_sample_pos USING gist (polygon);
CREATE INDEX sm_sectors_sample_pos_rectangle_spatial ON sm_sectors_sample_pos USING gist (rectangle);
CREATE INDEX sm_sectors_sample_pos_hopara_scope ON sm_sectors_sample_pos USING btree (hopara_scope);



